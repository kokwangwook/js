package com.example.salary;

public class Salary {
    private String grade;
    private int hoBon;
    private double salary2024;
    private double salary2025;

    // Getters and Setters

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public int getHoBon() {
        return hoBon;
    }

    public void setHoBon(int hoBon) {
        this.hoBon = hoBon;
    }

    public double getSalary2024() {
        return salary2024;
    }

    public void setSalary2024(double salary2024) {
        this.salary2024 = salary2024;
    }

    public double getSalary2025() {
        return salary2025;
    }

    public void setSalary2025(double salary2025) {
        this.salary2025 = salary2025;
    }
}
